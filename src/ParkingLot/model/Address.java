package ParkingLot.model;

public class Address {
    String street;
    String block;
    String city;
    String state;
    String country;

    public Address(String street, String block, String city, String state, String country){
        this.street = street;
        this.block = block;
        this.city = city;
        this.state = state;
        this.country = country;
    }

}
